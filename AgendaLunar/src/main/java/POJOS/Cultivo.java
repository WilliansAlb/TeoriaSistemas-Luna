public class Cultivo {
    private String idCultivo;
    private String tipo;

    public Cultivo() {
        this.idCultivo = "";
        this.tipo = "";
    }

    public String getIdCultivo() {
        return idCultivo;
    }

    public void setIdCultivo(String idCultivo) {
        this.idCultivo = idCultivo;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }
    
    
    
}
